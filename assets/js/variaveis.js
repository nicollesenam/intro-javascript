//como declarar
/* 

var - escopo global e local, pode ter seu valor alterado e se nao tiver um valor inicial será tratada como null/undefined

let - escopo local de bloco, pode ter seu valor alterado e se nao tiver um valor inicial será tratada como null/undefined

const - escopo global e local, somente leitura e o valor inicial é obrigatório e nao pode ser alterado


*/

//escopo
/* 
o escopo do javascript define a limitação e visibilidade de um bloco de código

escopo global - quando a variavel é declarada fora de qualquer bloco, sua visibilidade fica disponivel para todo codigo

escopo local - quando a variavel é declarada dentro de um bloco, sua visibilidade pode ficar disponivel ou nao

*/

//operadores lógicos

/* 
&& -"e" - considera que todos os valores sejam true
|| -"ou" - considera que qualquer valor seja true
! -"não" - inverte o valor de true para false ou vice-versa

*/